#!/usr/bin/env python3

'''
Given two strings and return a concatneted string.
'''


def concat(str1: str, str2: str) -> str:
    '''Concatnets two strings
        args:
            str1: first string
            str2: second string
        returns:
            str1 + str2: concatenated string.
    '''
    return str1 + str2
